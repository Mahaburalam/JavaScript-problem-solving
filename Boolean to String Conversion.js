// Create a function that takes a boolean variable flag and returns it as a string.
// Examples
/*
    boolToString(true) ➞ "true"
    boolToString(false) ➞ "false"    
*/

function toString(flag)
{
    const boolToString = flag.toString();
    return boolToString;
}
