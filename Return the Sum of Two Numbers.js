// Create a function that takes two numbers as arguments and returns their sum.

// Examples
/*
    addition(3, 2) ➞ 5
    addition(-3, -6) ➞ -9
    addition(7, 3) ➞ 10
*/

function addition(number1, number2)
{
    const output = (number1 + number2);
    return output;
}
